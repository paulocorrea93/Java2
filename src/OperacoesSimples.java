public class OperacoesSimples {

    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 2;

        int soma = num1 + num2;
        int subt = num1 - num2;
        int multi = num1 * num2;
        int divis = num1 / num2;
        int resta = num1 % num2;

        System.out.println(soma);
        System.out.println(subt);
        System.out.println(multi);
        System.out.println(divis);
        System.out.println(resta);

    }

}
