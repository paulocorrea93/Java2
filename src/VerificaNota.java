public class VerificaNota {

    public static void main(String[]args){

        double nota = 98;

        if(nota >= 90){
            System.out.println("Excelente");
        } else if (nota >= 70){
            System.out.println("Suficiente");
        } else {
            System.out.println("Nao passou");
        }

    }

}
